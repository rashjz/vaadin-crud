package rashjz.info.domain;

import javax.persistence.*;

/**
 * Created by Mobby on 2/13/2017.
 */
@Entity
@Table(name = "event_type", schema = "vaadin", catalog = "")
public class EventType {
    private int id;
    private String name;

    @Id
    @Column(name = "id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "name")
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        EventType eventType = (EventType) o;

        if (id != eventType.id) return false;
        if (name != null ? !name.equals(eventType.name) : eventType.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (name != null ? name.hashCode() : 0);
        return result;
    }
}
